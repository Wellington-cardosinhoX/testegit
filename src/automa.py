def soma(x, y):
    return x + y

resultado = soma(3, 5)
print(f"O resultado da soma é: {resultado}")


def multiplica(a, b):
    return a * b

resultado1 = multiplica(4, 6)
print(f"O resultado da multiplicação é: {resultado1}")

def subtrai(m, n):
    return m - n

resultado3 = subtrai(10, 4)
print(f"O resultado da subtração é: {resultado3}")